package Retos_Abril;

public class Reto_Dia4 {
    public static void main(String[] args) {
        /*
        * Un resplandor y hace ¡BOOM! y digo, ains ya está aquí la guerra.
        * Como dice la señora del famoso meme, un día estalló la guerra.
        * Haz un programa que, dado un número que se pasa por entrada, haga una
        * cuenta atrás y acabe con un ¡BOOM!.
        */
        for (int i = 5; i > 0; i--) {
            System.out.println(i);
        }

        System.out.println("¡BOOM!");
    }
}
